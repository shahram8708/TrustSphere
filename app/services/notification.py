"""Outbound notification service for TrustSphere."""

from __future__ import annotations

from datetime import datetime, timedelta
import html
import re
import sys

from flask import current_app, url_for


class NotificationService:
    """Send email and demo notification messages."""

    @classmethod
    def send_email(cls, to_email, subject, body_html, body_text=None):
        """Send an HTML email through Flask Mail."""
        try:
            from flask_mail import Message

            from app.extensions import mail

            msg = Message(
                subject=subject,
                recipients=[to_email],
                html=body_html,
                body=body_text or cls.strip_html(body_html),
            )
            mail.send(msg)
            return True, "Email sent"
        except Exception as exc:
            print(f"[NotificationService] Email send failed: {exc}", file=sys.stderr)
            return False, str(exc)

    @staticmethod
    def strip_html(html_string):
        """Convert simple HTML to plain text."""
        try:
            import bleach

            return bleach.clean(html_string or "", tags=[], strip=True)
        except Exception:
            return re.sub(r"<[^>]+>", "", html_string or "")

    @classmethod
    def send_alert_notification_email(cls, alert_id):
        """Notify an assigned analyst about an alert."""
        try:
            from app.models import AdminUser, Alert

            alert = Alert.query.get(alert_id)
            if not alert:
                return False, "Alert not found"
            analyst = alert.assigned_analyst
            if not analyst:
                analyst = AdminUser.query.filter(
                    AdminUser.institution_id == alert.institution_id,
                    AdminUser.role.in_(["security_analyst", "compliance_officer", "super_admin"]),
                    AdminUser._is_active.is_(True),
                ).first()
            if not analyst:
                return False, "No analyst email available"

            detail_url = url_for("admin.alert_detail", alert_id=alert.id, _external=True)
            body = cls.build_email_html(
                title=alert.title,
                body_paragraphs=[
                    f"Severity: {alert.severity.upper()}",
                    alert.description or "A TrustSphere alert requires analyst review.",
                ],
                cta_text="Open Alert",
                cta_url=detail_url,
                footer_note="This message was generated by TrustSphere alert monitoring.",
            )
            subject = f"[{alert.severity.upper()}] TrustSphere Alert: {alert.title}"
            return cls.send_email(analyst.email, subject, body)
        except Exception as exc:
            print(f"[NotificationService] Alert notification failed: {exc}", file=sys.stderr)
            return False, str(exc)

    @classmethod
    def send_password_reset_email(cls, user_type, user_id, reset_token):
        """Send a password reset email for admin or portal users.

        user_type: "admin" or "user"
        user_id: the primary key of the user record
        """
        try:
            if user_type == "admin":
                from app.models import AdminUser as _AdminUser

                user = _AdminUser.query.get(user_id)
            else:
                from app.models import User as _User

                user = _User.query.get(user_id)

            if not user or not getattr(user, "email", None):
                return False, "User not found or no email available"

            reset_url = url_for("auth.reset_password", token=reset_token, _external=True)
            # Use a generic, user-facing message for portal users and a slightly different
            # message for administrators to reflect their elevated access.
            if user_type == "admin":
                paragraphs = [
                    "We received a request to reset your TrustSphere administrator password.",
                    "This link expires in 1 hour. If you did not request this, ignore this email and review your account activity.",
                ]
            else:
                paragraphs = [
                    "We received a request to reset your TrustSphere account password.",
                    "This link expires in 1 hour. If you did not request this, you can ignore this email.",
                ]

            body = cls.build_email_html(
                title="Password Reset Request",
                body_paragraphs=paragraphs,
                cta_text="Reset Password",
                cta_url=reset_url,
                footer_note="Never share this link with anyone.",
            )
            return cls.send_email(
                user.email,
                "TrustSphere Password Reset Request",
                body,
            )
        except Exception as exc:
            print(f"[NotificationService] Password reset email failed: {exc}", file=sys.stderr)
            return False, str(exc)

